class Register

  def initialize(fullname, address, city, state, postal, country, email, user, password)
    @fullname = fullname
    @address = address
    @city = city
    @state = state
    @postal = postal
    @country = country
    @email = email
    @user = user
    @password = password
  end


  def fullname
    @fullname
  end

  def address
    @address
  end

  def city
    @city
  end

  def state
    @state
  end

  def postal
    @postal
  end

  def country
    @country
  end

  def email
    @email
  end

  def user
    @user
  end

  def password
    @password
  end


end
